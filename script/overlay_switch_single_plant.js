
  function contribution_on() {
    document.getElementById("overlay_contribution").style.display = "block";
    document.getElementById("body_list_contribution").style.overflow= "hidden";
  }
  function contribution_close() {
    document.getElementById("overlay_contribution").style.display = "none";
    document.getElementById("body_list_contribution").style.overflow= "scroll";
  }

  function submit_form_on() {
    document.getElementById("overlay_submit_form").style.display = "block";
    document.getElementById("body_list_contribution").style.overflow= "hidden";
  }
  function submit_form_off() {
    document.getElementById("overlay_submit_form").style.display = "block";
    document.getElementById("body_list_contribution").style.overflow= "hidden";
  }
